package com.ljf.company.day01;

/**
 * @author 龙江锋
 * package --包
 * 包名,全小写,公司的域名倒着写
 * 定义一个类
 * public  -- java里面的关键字，用来修饰类的访问权限
 * class -- java里面的关键字 ，用来定义类
 * Day01 --类名 -- 驼峰命名法
 * {java代码..}
 */

public class Day01 {
	/*
	 * public static void main(String[] args) {}
	 * 定义一个主方法/主函数/main方法 --用来作为程序的入口
	 * public --java里面的关键字，用来修饰方法的访问权限
	 * static --java里面的关键字，用来修饰方法是静态的
	 * void --java里面的关键字，用来修饰方法是没有返回值的
	 * main --方法名
	 * () --方法的标志
	 * String[] args --是方法的参数
	 * {} --java代码/方法体
	 */

	public static void main(String[] args) {

		//eclipse为 syso   alt+/也可以
		/*
		 * 向控制台指定的数据
		 * System --发出一个指令
		 * out --向外输出的动作
		 * println --打印之后再换行
		 * () --方法标志
		 * Day01 --方法参数
		 * ； --java语句结束符
		 * . --调用关系
		 */
		System.out.println("Day01");
	}
}


	